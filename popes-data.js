function displayPopes(popesArray) {
  const container = document.getElementById("popeCards");
  container.innerHTML = "";

  popesArray.forEach((pope) => {
    const card = document.createElement("div");
    card.className = "bg-white p-4 rounded shadow-md";
    card.innerHTML = `
      <img src="${pope.image}" alt="${pope.name}" class="w-full h-48 object-cover mb-2 rounded">
      <h3 class="text-xl font-bold">${pope.name}</h3>
      <p><strong>Reign:</strong> ${pope.reign}</p>
      <p><strong>Education:</strong> ${pope.education}</p>
      <p><strong>Ordained:</strong> ${pope.ordained}</p>
      <p><strong>Parishes:</strong> ${pope.parishes.join(", ")}</p>
      <p><strong>Family:</strong> ${pope.family}</p>
      <p><strong>Milestones:</strong> ${pope.milestones}</p>
      <img src="${pope.churchImage}" alt="Church served by ${pope.name}" class="w-full h-40 object-cover mt-2 rounded">
    `;
    container.appendChild(card);
  });
}

document.getElementById("searchInput").addEventListener("input", (e) => {
  const filtered = popes.filter((pope) =>
    pope.name.toLowerCase().includes(e.target.value.toLowerCase())
  );
  displayPopes(filtered);
});

window.onload = () => displayPopes(popes);
